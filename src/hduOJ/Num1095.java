package hduOJ;

import java.util.Scanner;

public class Num1095 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        while (sc.hasNext()) {
            int sum = sc.nextInt() + sc.nextInt();
            System.out.println(sum);
            System.out.println();
        }
    }
}
